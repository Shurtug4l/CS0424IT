#include <stdio.h>

int main() {
  // Dichiarazione e inizializzazione di variabili
  int intero = 10;
  float decimale = 5.75;
  char carattere = 'A';

  // Stampa dei valori sulla console
  printf("Valore dell'intero: %d\n", intero);
  printf("Valore del float: %.2f\n", decimale);
  printf("Valore del carattere: %c\n", carattere);

  return 0;
}
