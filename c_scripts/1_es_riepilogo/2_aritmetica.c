#include <stdio.h>

int main() {
  int a = 10, b = 5;

  // Operazioni aritmetiche
  printf("Addizione: %d + %d = %d\n", a, b, a + b);
  printf("Sottrazione: %d - %d = %d\n", a, b, a - b);
  printf("Moltiplicazione: %d * %d = %d\n", a, b, a * b);
  printf("Divisione: %d / %d = %d\n", a, b, a / b);

  return 0;
}
